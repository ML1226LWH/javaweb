package swu.lwh;

public class Logs {
    private int id;
    private String users;
    private String pwd;

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }
    public void setUsers(String users){
        this.users= users;
    }
    public String getUsers(){
        return users;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public String getPwd() {
        return pwd;
    }
}
